package org.aa.drug;

public class Package {

	Long DrugCode;
	String Upc;
	String PackageSizeUnit;
	String PackageType;
	String PackageSize;
	String ProductInformation;
	public Long getDrugCode() {
		return DrugCode;
	}
	public void setDrugCode(Long drugCode) {
		DrugCode = drugCode;
	}
	public String getUpc() {
		return Upc;
	}
	public void setUpc(String upc) {
		Upc = upc;
	}
	public String getPackageSizeUnit() {
		return PackageSizeUnit;
	}
	public void setPackageSizeUnit(String packageSizeUnit) {
		PackageSizeUnit = packageSizeUnit;
	}
	public String getPackageType() {
		return PackageType;
	}
	public void setPackageType(String packageType) {
		PackageType = packageType;
	}
	public String getPackageSize() {
		return PackageSize;
	}
	public void setPackageSize(String packageSize) {
		PackageSize = packageSize;
	}
	public String getProductInformation() {
		return ProductInformation;
	}
	public void setProductInformation(String productInformation) {
		ProductInformation = productInformation;
	}
	
	
}
