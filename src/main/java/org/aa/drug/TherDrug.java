package org.aa.drug;

public class TherDrug {

	Long DrugCode;
	String TcAtcNumber;
	String TcAtc;
	String TcAhfsNumber;
	String TcAhfs;
	public Long getDrugCode() {
		return DrugCode;
	}
	public void setDrugCode(Long drugCode) {
		DrugCode = drugCode;
	}
	public String getTcAtcNumber() {
		return TcAtcNumber;
	}
	public void setTcAtcNumber(String tcAtcNumber) {
		TcAtcNumber = tcAtcNumber;
	}
	public String getTcAtc() {
		return TcAtc;
	}
	public void setTcAtc(String tcAtc) {
		TcAtc = tcAtc;
	}
	public String getTcAhfsNumber() {
		return TcAhfsNumber;
	}
	public void setTcAhfsNumber(String tcAhfsNumber) {
		TcAhfsNumber = tcAhfsNumber;
	}
	public String getTcAhfs() {
		return TcAhfs;
	}
	public void setTcAhfs(String tcAhfs) {
		TcAhfs = tcAhfs;
	}
	
	
}
