package org.aa.drug;

public class PharmaStd {

	Long DrugCode;
	String PharmaceuticalStd;
	
	public Long getDrugCode() {
		return DrugCode;
	}
	public void setDrugCode(Long drugCode) {
		DrugCode = drugCode;
	}
	public String getPharmaceuticalStd() {
		return PharmaceuticalStd;
	}
	public void setPharmaceuticalStd(String pharmaceuticalStd) {
		PharmaceuticalStd = pharmaceuticalStd;
	}
	
	
	
}
