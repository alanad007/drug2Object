package org.aa.drug;

public class Ingredient {

	Long DrugCode;
	Long ActiveIngredientCode;
	String Ingredient;
	String IngredientSuppliedInd;
	String Strength;
	String StrengthUnit;
	String StrengthType;
	String DosageValue;
	String Base;
	String DosageUnit;
	String Notes;
	public Long getDrugCode() {
		return DrugCode;
	}
	public void setDrugCode(Long drugCode) {
		DrugCode = drugCode;
	}
	public Long getActiveIngredientCode() {
		return ActiveIngredientCode;
	}
	public void setActiveIngredientCode(Long activeIngredientCode) {
		ActiveIngredientCode = activeIngredientCode;
	}
	public String getIngredient() {
		return Ingredient;
	}
	public void setIngredient(String ingredient) {
		Ingredient = ingredient;
	}
	public String getIngredientSuppliedInd() {
		return IngredientSuppliedInd;
	}
	public void setIngredientSuppliedInd(String ingredientSuppliedInd) {
		IngredientSuppliedInd = ingredientSuppliedInd;
	}
	public String getStrength() {
		return Strength;
	}
	public void setStrength(String strength) {
		Strength = strength;
	}
	public String getStrengthUnit() {
		return StrengthUnit;
	}
	public void setStrengthUnit(String strengthUnit) {
		StrengthUnit = strengthUnit;
	}
	public String getStrengthType() {
		return StrengthType;
	}
	public void setStrengthType(String strengthType) {
		StrengthType = strengthType;
	}
	public String getDosageValue() {
		return DosageValue;
	}
	public void setDosageValue(String dosageValue) {
		DosageValue = dosageValue;
	}
	public String getBase() {
		return Base;
	}
	public void setBase(String base) {
		Base = base;
	}
	public String getDosageUnit() {
		return DosageUnit;
	}
	public void setDosageUnit(String dosageUnit) {
		DosageUnit = dosageUnit;
	}
	public String getNotes() {
		return Notes;
	}
	public void setNotes(String notes) {
		Notes = notes;
	}
	
	
}
